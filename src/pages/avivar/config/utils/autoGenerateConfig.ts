/**
 * Auto-gera configurações de IA baseado no template/subnicho selecionado
 * Isso permite que o usuário não precise configurar manualmente a personalidade, instruções, etc.
 */

import { SubnichoType, NichoType, TomVoz, FluxoAtendimento } from '../types';

interface AutoGeneratedConfig {
  aiIdentity: string;
  aiObjective: string;
  aiInstructions: string;
  aiRestrictions: string;
  toneOfVoice: TomVoz;
  fluxoAtendimento: FluxoAtendimento;
}

