interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
}

export function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0);

  return (
    <div className="w-80 bg-[hsl(var(--avivar-card))] border border-[hsl(var(--avivar-border))] rounded-lg shadow-xl overflow-hidden">
      {/* Category tabs */}
      <div className="flex border-b border-[hsl(var(--avivar-border))] bg-[hsl(var(--avivar-muted)/0.3)]">
        {EMOJI_CATEGORIES.map((category, index) => (
          <button
            key={category.name}
            onClick={() => setActiveCategory(index)}
            className={cn(
              "flex-1 p-2 text-lg transition-colors hover:bg-[hsl(var(--avivar-muted))]",
              activeCategory === index && "bg-[hsl(var(--avivar-muted))]"
            )}
            title={category.name}
          >
            {category.icon}
          </button>
        ))}
      </div>

      {/* Emoji grid */}
      <div className="h-64 overflow-y-auto p-2">
        <div className="grid grid-cols-8 gap-1">
          {EMOJI_CATEGORIES[activeCategory].emojis.map((emoji, index) => (
            <button
              key={`${emoji}-${index}`}
              onClick={() => onSelect(emoji)}
              className="p-1.5 text-xl hover:bg-[hsl(var(--avivar-muted))] rounded transition-colors"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
