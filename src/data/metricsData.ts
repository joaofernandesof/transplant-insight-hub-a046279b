export interface WeekData {
  weekNumber: number;
  weekLabel: string;
  startDate: Date;
  endDate: Date;
  isAvailable: boolean;
  isFilled: boolean;
  values: Record<string, number | string | null>;
}

// Gerar semanas de 2026
export function generateWeeks2026(): WeekData[] {
  const weeks: WeekData[] = [];
  const startOfYear = new Date(2026, 0, 5); // Primeira segunda-feira de 2026
  
  for (let i = 0; i < 52; i++) {
    const startDate = new Date(startOfYear);
    startDate.setDate(startDate.getDate() + (i * 7));
    
    const endDate = new Date(startDate);
    endDate.setDate(endDate.getDate() + 6);
    
    weeks.push({
      weekNumber: i + 1,
      weekLabel: `Semana ${i + 1}`,
      startDate,
      endDate,
      isAvailable: false,
      isFilled: false,
      values: {}
    });
  }
  
  return weeks;
}

export function isWeekAvailable(week: WeekData): boolean {
  const now = new Date();
  const mondayAfterWeek = new Date(week.endDate);
  mondayAfterWeek.setDate(mondayAfterWeek.getDate() + 1);
  mondayAfterWeek.setHours(0, 0, 0, 0);
  
  return now >= mondayAfterWeek;
}

export function getLastAvailableWeek(weeks: WeekData[]): WeekData | null {
  const availableWeeks = weeks.filter(w => isWeekAvailable(w));
  return availableWeeks.length > 0 ? availableWeeks[availableWeeks.length - 1] : null;
}

export function formatDate(date: Date): string {
  return date.toLocaleDateString('pt-BR', { day: '2-digit', month: '2-digit' });
}
