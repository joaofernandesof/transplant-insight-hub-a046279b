Deno.serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders })
  }

  try {
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    const supabase = createClient(supabaseUrl, supabaseServiceKey)

    // First, clear all existing daily metrics
    const { error: deleteError } = await supabase
      .from('daily_metrics')
      .delete()
      .not('id', 'is', null)

    if (deleteError) {
      console.error('Error deleting existing metrics:', deleteError)
    }

    // Build aggregated data by clinic and date
    const metricsMap: Record<string, Record<string, Record<string, number | null>>> = {}

    for (const item of spreadsheetData) {
      const clinicId = clientMapping[item.client]
      if (!clinicId) {
        console.log(`Client not found: ${item.client}`)
        continue
      }
      
      const indicatorName = indicatorMap[item.indicator]
      if (!indicatorName) {
        console.log(`Indicator not found: ${item.indicator}`)
        continue
      }

      if (!metricsMap[clinicId]) {
        metricsMap[clinicId] = {}
      }

      for (const [date, value] of Object.entries(item.values)) {
        if (!metricsMap[clinicId][date]) {
          metricsMap[clinicId][date] = {}
        }
        metricsMap[clinicId][date][indicatorName] = value
      }
    }

    // Insert all metrics
    const insertData = []
    for (const [clinicId, dates] of Object.entries(metricsMap)) {
      for (const [date, metrics] of Object.entries(dates)) {
        insertData.push({
          clinic_id: clinicId,
          metric_date: date,
          leads_novos: metrics.leads_novos ?? null,
          tempo_uso_atendente: metrics.tempo_uso_atendente ?? null,
          atividades_atendente: metrics.atividades_atendente ?? null,
          atividades_robo: metrics.atividades_robo ?? null,
          mensagens_enviadas_atendente: metrics.mensagens_enviadas_atendente ?? null,
          mensagens_enviadas_robo: metrics.mensagens_enviadas_robo ?? null,
          mensagens_recebidas: metrics.mensagens_recebidas ?? null,
          tarefas_realizadas: metrics.tarefas_realizadas ?? null,
          tarefas_atrasadas: metrics.tarefas_atrasadas ?? null,
          agendamentos: metrics.agendamentos ?? null,
          vendas_realizadas: metrics.vendas_realizadas ?? null,
          leads_descartados: metrics.leads_descartados ?? null,
        })
      }
    }

    const { error: insertError, data: insertedData } = await supabase
      .from('daily_metrics')
      .insert(insertData)
      .select()

    if (insertError) {
      console.error('Error inserting metrics:', insertError)
      return new Response(
        JSON.stringify({ success: false, error: insertError.message }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
      )
    }

    return new Response(
      JSON.stringify({ 
        success: true, 
        message: `Imported ${insertedData?.length || 0} daily metrics records`,
        clients: Object.keys(metricsMap).length
      }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    )

  } catch (error: unknown) {
    console.error('Error:', error)
    const errorMessage = error instanceof Error ? error.message : 'Unknown error'
    return new Response(
      JSON.stringify({ success: false, error: errorMessage }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
    )
  }
})
